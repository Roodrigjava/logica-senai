package execicios;

public class Ex009MultiTriangulo {
	public static void main(String[] args) {
		
		int i = 0;
		String x = "";
		String y = "";

		while (i < 3) {
			x += "*";
			
			System.out.println(x);
			i++;
			
			
		}

		int j = 0;
		while (j < 3) {
			y += "*";
			System.out.println(y);
			++j;
		

	}

}
}
